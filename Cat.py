class Cat:
    class_name = "Cat"

    def __init__(self, wool_color, eyes_color, name):
        self.wool_color = wool_color
        self.eyes_color = eyes_color
        self.name = name

    def purr(self):
        print("Meow")

    def hiss(self):
        print("Shhhh")

    def scrable(self):
        print("Scrabble")


